package com.example.baraa.app;


public class List_Data {
    private String name;
    private String moviename;

    public List_Data(String name, String moviename) {
        this.name = name;
        this.moviename = moviename;
    }

    public String getName() {
        return name;
    }

    public String getMoviename() {
        return moviename;
    }
}